<script>
import axios from "axios";

export default {
  data: function () {
    return {
      newSessionParams: {},
      errors: [],
    };
  },
  methods: {
    submit: function () {
      axios
        .post("/sessions", this.newSessionParams)
        .then((response) => {
          axios.defaults.headers.common["Authorization"] = "Bearer " + response.data.jwt;
          console.log(response.data);
          localStorage.setItem("jwt", response.data.jwt);
          this.$router.push("/equipment-sets");
        })
        .catch((error) => {
          console.log(error.response);
          this.errors = ["Invalid email or password."];
          this.email = "";
          this.password = "";
        });
    },
  },
};
</script>

<template>
  <div class="container-fluid form-body">
    <form class="form-signin center" v-on:submit.prevent="submit()">
      <img class="mb-4" src="/images/dragon-solid.svg" alt="" width="72" height="72" />
      <h1 class="h3 mb-3 font-weight-normal">Please Log In</h1>
      <hr class="featurette-divider" />
      <label for="inputEmail" class="sr-only">Email address</label>
      <input
        type="email"
        id="inputEmail"
        class="form-control"
        placeholder="Email address"
        required
        autofocus
        v-model="newSessionParams.email"
      />
      <label for="inputPassword" class="sr-only">Password</label>
      <input
        type="password"
        id="inputPassword"
        class="form-control"
        placeholder="Password"
        required
        v-model="newSessionParams.password"
      />
      <p class="text-danger" v-for="error in errors" :key="error.id">{{ error }}</p>
      <input type="submit" class="btn btn-secondary btn-lg btn-block" style="width: 100%" value="Login" />
    </form>
  </div>
</template>

